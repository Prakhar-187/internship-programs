    if(i==1 or i==7):
        print("*******")
    elif(i==2):
        print(" "*5,end="")
        print("*")
    elif(i==3):
        print(" "*4,end="")
        print("*")
    elif(i==4):
        print(" "*3,end="")
        print("*")
    elif(i==5):
        print(" "*2,end="")
        print("*")
    elif(i==6):
        print(" "*1,end="")
        print("*")